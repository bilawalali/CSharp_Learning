using System;
using System.Collections.Generic;
using System.Text;

namespace TestProgram1.Math
{
    public class Calculator
    {
        public int Add(int a, int b)
        {
            return a + b;        
        }

        public int Sub(int c, int d)
        {
            return c - d;
        }

        public int Div(int e, int f)
        {
            return e / f;
        }

        public int mult(int g, int h)
        {
            return g * h;
        }
        
    }
}
